    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String bookName=jTextField1.getText();
        String publisher=jTextField2.getText();
        String description=jTextArea1.getText();
        String noOfPages= jTextField3.getText();
        String catagory = (String) jComboBox1.getSelectedItem();
       
        if(bookName.isEmpty()||publisher.isEmpty()|| description.isEmpty()|| noOfPages.isEmpty()|| catagory.isEmpty()){
            JOptionPane.showMessageDialog(this,"ERROR:All Fields Are Mandatoryr \nEnter all other Fields first");
        }
        else{
        JFrame parentFrame = new JFrame();

        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter txtFilter = new FileNameExtensionFilter("txt files","txt");
        FileNameExtensionFilter txtFilter2 = new FileNameExtensionFilter("ePUB files","epub");
        FileNameExtensionFilter txtFilter3 = new FileNameExtensionFilter("PDF files","pdf");
        fileChooser.setDialogTitle("Specify a file to Upload");
        fileChooser.setFileFilter(txtFilter);
        fileChooser.setFileFilter(txtFilter2);
        fileChooser.setFileFilter(txtFilter3);
        int userSelection = fileChooser.showSaveDialog(parentFrame);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            String srcFile = fileToSave.getAbsolutePath();
            destFileName = fileToSave.getName();
            File destinationFile = new File("C:\\Users\\Abrit\\Desktop\\class\\third sem\\java\\mini project\\Book_manager\\uploadedFiles\\"+destFileName);
                //        System.out.println("Save as file: " + fileToSave.getAbsolutePath());
                try{
                    Files.copy(fileToSave.toPath(),destinationFile.toPath());
                    System.out.println("copied");
                }
                catch(IOException e){
                    JOptionPane.showMessageDialog(this,e.getMessage());
                }
                System.out.println(srcFile+" "+destFileName);
                addFileToTable(destFileName,bookName);
        }
    }//GEN-LAST:event_jButton3ActionPerformed
}
    void addFileToTable(String destFileName,String bookName){
        try{
            Class.forName("java.sql.DriverManager");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/book_manager","root","1234");
            String query = "update books set file=? where bookName=?";
            PreparedStatement st= con.prepareStatement(query);
            st.setString(1,destFileName);
            st.setString(2,bookName);
            st.execute();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
